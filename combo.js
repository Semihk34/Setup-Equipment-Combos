function randomSec(dizi){
    return dizi[Math.floor(Math.random() * dizi.length)];
}

function comboOlustur(){

    const butce = Number(document.getElementById("butce").value);

    const minFiyat =
        Math.min(...mouseler.map(x => x.fiyat)) +
        Math.min(...klavyeler.map(x => x.fiyat)) +
        Math.min(...mousepadler.map(x => x.fiyat));

  if (butce < minFiyat) {
        document.getElementById("sonuc").innerHTML = `
            <div class="error-kart" style="border: 1px solid #ddd; background: #fff; box-shadow: 0 10px 30px rgba(0,0,0,0.05);">
                <h2 style="color: #283044; font-size: 22px;">Yetersiz Bütçe</h2>
                <div style="width: 40px; height: 2px; background: #78A1BB; margin: 15px auto;"></div>
                <p style="color: #666; font-size: 16px;">Minimum kombinasyon tutarı:</p>
                <p style="font-size: 24px; font-weight: bold; color: #283044; margin-top: 5px;">${minFiyat} TL</p>
                <button onclick="location.reload()" style="margin-top: 20px; background: #283044; color: white; font-size: 14px; padding: 8px 15px;">Tekrar Dene</button>
            </div>
        `;
        return;
    }

    let mouse, klavye, mousepad;
    let toplam = 0;

    let deneme = 0;

    do{
        mouse = randomSec(mouseler);
        klavye = randomSec(klavyeler);
        mousepad = randomSec(mousepadler);

        toplam = mouse.fiyat + klavye.fiyat + mousepad.fiyat;

        deneme++;

        if(deneme > 2000) break;

    }while(toplam > butce);

    document.getElementById("sonuc").innerHTML = `
    <div class="kart">
        <img src="${mouse.resim}">
        <h2>${mouse.isim}</h2>
        <p>${mouse.fiyat} TL</p>
        <a href="${mouse.link}" target="_blank"><button>Ürüne Git</button></a>
    </div>

    <div class="kart">
        <img src="${klavye.resim}">
        <h2>${klavye.isim}</h2>
        <p>${klavye.fiyat} TL</p>
        <a href="${klavye.link}" target="_blank"><button>Ürüne Git</button></a>
    </div>

    <div class="kart">
        <img src="${mousepad.resim}">
        <h2>${mousepad.isim}</h2>
        <p>${mousepad.fiyat} TL</p>
        <a href="${mousepad.link}" target="_blank"><button>Ürüne Git</button></a>
    </div>

    <h3 class="toplam">Toplam: ${toplam} TL</h3>
    `;
}

